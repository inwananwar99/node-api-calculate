const sum = (val1,val2)=>{
    const result = val1+val2
    return result
}

const substraction = (val1,val2)=>{
    const result = val1-val2
    return result
}

const multiplication = (val1,val2)=>{
    const result = val1*val2
    return result
}

const division = (val1,val2)=>{
    const result = val1/val2
    return result
} 

export default{
    sum,substraction,multiplication,division
}